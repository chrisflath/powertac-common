package org.powertac.common;

import org.joda.time.Instant;
import org.powertac.common.transformer.BrokerConverter;
import com.thoughtworks.xstream.annotations.*;

/**
 * Represents interest charges and payments for brokers. Generated by
 * AccountingService once/day.
 * @author John Collins
 */
@XStreamAlias("bank-tx")
class BankTransaction
{
  private int id;
  
  /** The broker for this transactions */
  @XStreamAsAttribute
  private String brokerId;
  
  /** The amount of this transaction */
  @XStreamAsAttribute
  private double amount = 0.0;
  
  /** When this transaction occurred  */
  private Instant postedTime;

  /**
   * Constructs a new BankTransaction instance, giving it a new id.
   */
  public BankTransaction (Broker broker, double amount, Instant time)
  {
    this.id = makeId();
    this.brokerId = broker.getId();
    this.amount = amount;
    this.postedTime = time;
  }

  /**
   * Reconstructs a BankTransaction instance from message or logfile.
   * Do we need this???
   */
  public BankTransaction (int id, String brokerId, double amount, Instant time)
  {
    this.id = id;
    this.brokerId = brokerId;
    this.amount = amount;
    this.postedTime = time;
  }

  public int getId ()
  {
    return id;
  }

  public String getBrokerId ()
  {
    return brokerId;
  }

  public double getAmount ()
  {
    return amount;
  }

  public Instant getPostedTime ()
  {
    return postedTime;
  }
  
  // static ID management
  private static int idValue = 0;
  
  private static synchronized int makeId ()
  {
    return idValue++;
  }
}
